public class Member {


	public String ID;
	public String firstName;
	public String lastName;	
	public Integer captainCheck;	
	public Integer yearsOfSailingExp;


	public Member(String ID, String firstName , String lastName, Integer captainCheck, Integer yearsOfSailingExp)
	{
	this.ID = ID;
	this.firstName = firstName;
	this.lastName = lastName;
	this.captainCheck = captainCheck;
	this.yearsOfSailingExp = yearsOfSailingExp;	
	
	}

}
